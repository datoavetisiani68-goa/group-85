#სხვა შემთხვევაში დაბეჭდე --> "რიცხვი უარყოფითია"


number=int(input("ჩაწერე რიცხვი:"))
if number>=0:
    print("შიგნით შეამოწმე")
    if number %2==0:
        print("დადებითი ლუწი")
    elif number %2==1:
        print("დადებითი კენტი")
else:
    print("რიცხვი უარყოფითია")

