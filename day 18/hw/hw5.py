#სხვა შემთხვევაში დაბეჭდე - "დაბალი ქულა"

coin=int(input("ჩაწერე ქულა 0-დან 100-დე"))

if coin>=50:
    print("შიგნით შეამოწმე")
    if coin>90:
        print("შესანიშნავი")
    else:
        print("შესანიშნავი")
else:
    print("დაბალი ქულა")
