# ცალი რიცხვი გვხვდება სიაში რომელიც არის 50 ზე მეტი


numbers = [10, 55, 80, 23, 60, 49, 100]

count = 0
for num in numbers:
    if num > 50:
        count += 1

print("50-ზე მეტი რიცხვების რაოდენობა:", count)

