# ამ რიცხვში მყოოფი ციფრების ჯამი, მაგ გვაქვს "751" შენი 
# დავალებაა გაიგო ამ რიცხვში მყოფო ციფრების ჯამი ანუ ---> 7 + 5 + 1 = 13 


number = "751"
sum_digits = 0

for digit in number:
    sum_digits += int(digit)

print("ციფრების ჯამი:", sum_digits)
