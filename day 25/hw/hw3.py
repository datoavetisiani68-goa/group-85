# 3) შექმენი list: nums = [1, 2, 3, 2, 4, 2, 5] 
# მომხმარებელს შემოაყვანინე რიცხვი, count()-ით დაითვალე რამდენჯერ გვხვდება ეს რიცხვი, დაბეჭდე პასუხი

nums = [1, 2, 3, 2, 4, 2, 5]

num = int(input("შეიყვანე რიცხვი: "))

result = nums.count(num)

print("რიცხვი",num ,"სიაში გვხვდება",result ,"-ჯერ")

